package cn.sanbu.avalon.endpoint3.director.structures;

public class StaticEvent {
    public final String name;
    public final int id;
    public final String value;
    public final String top;

    public StaticEvent(String name, int id, String value, String top) {
        this.name = name;
        this.id = id;
        this.value = value;
        this.top = top;
    }
}
